// ── Helper functions ──────────────────────────────────────────────

/** Get all activities for a specific city */
export function getActivitiesByCity(cityId: string): Activity[] {
    return ACTIVITIES.filter(a => a.cityId === cityId)
}

/** Get all activities at a specific place */
export function getActivitiesByPlace(placeId: string): Activity[] {
    return ACTIVITIES.filter(a => a.placeId === placeId)
}

/** Look up an activity by its slug within a city */
export function getActivityBySlug(cityId: string, slug: string): Activity | undefined {
    return ACTIVITIES.find(a => a.cityId === cityId && a.slug === slug)
}

/** Look up tag metadata by slug */
export function getTagBySlug(slug: string): TagMeta | undefined {
    return TAG_META.find(t => t.slug === slug)
}

/** Get activities for a specific tag within a city */
export function getActivitiesByCityAndTag(cityId: string, tagName: string): Activity[] {
    return ACTIVITIES.filter(a => a.cityId === cityId && a.tags?.includes(tagName))
}

/** Get unique tags for a city's activities */
export function getTagsByCity(cityId: string): string[] {
    const cityActivities = getActivitiesByCity(cityId)
    const tagSet = new Set<string>()
    cityActivities.forEach(a => a.tags?.forEach(t => tagSet.add(t)))
    return Array.from(tagSet)
}


